								<h3 class="text-green">Quelles sont les causes ?</h3>
								<br>

								<p class="text-justify text-dark"><strong>Cette pénurie est principalement due à deux facteurs :</strong>
									<ul >
										<li>Le premier est le confinment. De nombreuses usines de production ont dû cesser ou ralentir fortement leurs activités. A leurs réouvertures, ces entreprises ont accumulé un retard conséquent qu’elles mettront plusieurs mois à combler. Un vélo complet nécessite environ 17 intervenants. Chaque intermédiaire a  accusé un léger retard, la chaine complète a donc été retardée et le retard sur la livraison finale devient donc conséquent.</li>
										<li>Le second facteur est l’explosion de la demande de vélo. Les premières mesures de confinement, ayant obligées la population à rester chez elle, ont fait prendre conscience à de nombreuses personnes de l’importance des activités extérieures. C’est pourquoi, dès l’assouplissement des règles et la réouverture des magasins, les revendeurs de vélos ont été pris d’assaut par les usagers. Entre mai et juin les ventes de vélos ont subi une hausse de 117% par rapport à la même période de l’année précédente. </li>
									</ul>
								</p>
